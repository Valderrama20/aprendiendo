// "macros" los cuales podemos ubicar en la libreria estardar "std::fmt"
// algunos de estos son:

// format!: Escribe texto formateado en string
// print!: Igual que "format!" pero el texto se imprime en la consola (io::stdout)
// println!: Igual que "print!" pero el agrega una nueva linea
// eprint!: igual que print! pero el texto se imprime según el error estándar (io::stderr) 
// lo utilizamos para imprimir errores.
// eprintln!: igual que eprint! pero se añade una nueva línea.


